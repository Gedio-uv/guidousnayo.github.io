/**
 * Detect the user's native language code (2-letter).
 * Falls back to 'en'.
 */
export function detectNativeLanguage() {
  const lang = (navigator.language || navigator.userLanguage || 'en').toLowerCase();
  const primary = lang.split('-')[0];
  const supported = Object.keys(TRANSLATIONS).filter(k => k !== 'de');
  return supported.includes(primary) ? primary : 'en';
}

/**
 * Get all translations for a given language code (or advanced mode).
 * @param {string} langCode - e.g. 'es', 'en', 'de'
 */
export function getTranslations(langCode) {
  return TRANSLATIONS[langCode] || TRANSLATIONS['en'];
}

/**
 * Get a single translation string.
 * @param {string} key
 * @param {string} langCode
 */
export function t(key, langCode) {
  const trans = TRANSLATIONS[langCode] || TRANSLATIONS['en'];
  return trans[key] || TRANSLATIONS['en'][key] || key;
}
