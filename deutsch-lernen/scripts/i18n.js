/**
 * Detect the user's native language from the browser/system.
 * Scans navigator.languages (full priority list) so multi-language
 * phones (e.g. German + Chinese) pick the right language.
 * Falls back to 'en'.
 */
export function detectNativeLanguage() {
  const supported = Object.keys(TRANSLATIONS).filter(k => k !== 'de');

  // Check all preferred languages in priority order
  const candidates = [
    ...(navigator.languages || []),
    navigator.language,
    navigator.userLanguage,
  ].filter(Boolean);

  for (const lang of candidates) {
    const primary = lang.toLowerCase().split('-')[0];
    if (supported.includes(primary)) return primary;
  }

  return 'en';
}

/**
 * Get all translations for a given language code (or advanced mode).
 * @param {string} langCode - e.g. 'es', 'en', 'de'
 */
export function getTranslations(langCode) {
  return TRANSLATIONS[langCode] || TRANSLATIONS['en'];
}

/**
 * Get a single translation string.
 * @param {string} key
 * @param {string} langCode
 */
export function t(key, langCode) {
  const trans = TRANSLATIONS[langCode] || TRANSLATIONS['en'];
  return trans[key] || TRANSLATIONS['en'][key] || key;
}
